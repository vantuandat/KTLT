import tkinter as tk

# Tạo cửa sổ chính
window = tk.Tk()

# Đặt tiêu đề cho cửa sổ
window.title("Cửa sổ đồ họa Tkinter")

# Đặt kích thước cho cửa sổ (width x height)
window.geometry("400x300")

# Đặt một số thuộc tính cho cửa sổ (tuỳ chọn)
window.configure(bg="lightblue")  # Thay đổi màu nền của cửa sổ

# Hiển thị cửa sổ
window.mainloop()
